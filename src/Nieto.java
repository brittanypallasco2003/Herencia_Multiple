public class Nieto {
    //ATRIBUTOS
    int pecas;
    String color_pecas;

    public Nieto(int pecas, String color_pecas) {
        this.pecas = pecas;
        this.color_pecas = color_pecas;
    }

    public int getPecas() {
        return pecas;
    }

    public void setPecas(int pecas) {
        this.pecas = pecas;
    }

    public String getColor_pecas() {
        return color_pecas;
    }

    public void setColor_pecas(String color_pecas) {
        this.color_pecas = color_pecas;
    }
}
