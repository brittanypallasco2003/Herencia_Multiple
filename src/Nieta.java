public class Nieta extends Hijo{
    //ATRIBUTOS
    String forma_ojos;
    int largo_nariz;

    public Nieta(String forma_ojos, int largo_nariz) {
        super();
        this.forma_ojos = forma_ojos;
        this.largo_nariz = largo_nariz;
    }

    public String getForma_ojos() {
        return forma_ojos;
    }

    public void setForma_ojos(String forma_ojos) {
        this.forma_ojos = forma_ojos;
    }

    public int getLargo_nariz() {
        return largo_nariz;
    }

    public void setLargo_nariz(int largo_nariz) {
        this.largo_nariz = largo_nariz;
    }
}
